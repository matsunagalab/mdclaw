def _json_check(check_id: str, path: str, equals, weight: float = 1.0) -> dict:
    return {
        "check_id": check_id,
        "check_type": "json_equals",
        "json_file": "metrics.json",
        "json_path": path,
        "equals": equals,
        "weight": weight,
    }


def _json_min_length(check_id: str, path: str, minimum: int, weight: float = 1.0) -> dict:
    return {
        "check_id": check_id,
        "check_type": "json_min_length",
        "json_file": "metrics.json",
        "json_path": path,
        "min_length": minimum,
        "weight": weight,
    }


def _provenance_text_check(
    check_id: str,
    required_text_groups: list[list[str]],
    weight: float = 1.0,
    text_files: list[str] | None = None,
) -> dict:
    return {
        "check_id": check_id,
        "check_type": "artifact_provenance_text",
        "text_files": text_files or ["provenance.json", "evidence_report.json"],
        "required_text_groups": required_text_groups,
        "weight": weight,
    }


def _component_check(
    check_id: str,
    *,
    min_counts: dict[str, int] | None = None,
    max_counts: dict[str, int] | None = None,
    exact_counts: dict[str, int] | None = None,
    residue_aliases: dict[str, list[str]] | None = None,
    weight: float = 1.0,
) -> dict:
    out = {
        "check_id": check_id,
        "check_type": "structure_component_rescan",
        "structure_manifest_path": "outputs.prepared_structure",
        "structure_path": "prepared_structure.pdb",
        "weight": weight,
    }
    if min_counts:
        out["min_residue_counts"] = min_counts
    if max_counts:
        out["max_residue_counts"] = max_counts
    if exact_counts:
        out["exact_residue_counts"] = exact_counts
    if residue_aliases:
        out["residue_aliases"] = residue_aliases
    return out


def _residue_check(
    check_id: str,
    chain: str,
    number: str,
    resname: str,
    *,
    atoms: list[str] | None = None,
    weight: float = 1.0,
) -> dict:
    out = {
        "check_id": check_id,
        "check_type": "pdb_residue_state",
        "structure_manifest_path": "outputs.prepared_structure",
        "structure_path": "prepared_structure.pdb",
        "residue_chain": chain,
        "residue_number": number,
        "required_residue_name": resname,
        "weight": weight,
    }
    if atoms:
        out["required_atom_names"] = atoms
    return out


def _topology_minimization_checks() -> list[dict]:
    return [
        {
            "check_id": "topology_artifact_bundle_present",
            "check_type": "topology_artifact_bundle",
            "topology_manifest_path": "outputs.topology",
            "min_topology_artifact_count": 1,
            "weight": 1.0,
        },
        {
            "check_id": "openmm_system_loads_when_applicable",
            "check_type": "openmm_system_load",
            "topology_manifest_path": "outputs.topology",
            "weight": 1.0,
        },
        {
            "check_id": "openmm_energy_rescan_when_applicable",
            "check_type": "openmm_energy_rescan",
            "topology_manifest_path": "outputs.topology",
            "weight": 1.0,
        },
        {
            "check_id": "minimization_report_confirms_completion",
            "check_type": "minimization_report_check",
            "minimization_report_manifest_path": "outputs.minimization_report",
            "weight": 1.0,
        },
    ]


def _minimized_persistence_checks(checks: list[dict]) -> list[dict]:
    out: list[dict] = []
    for check in checks:
        if check.get("check_type") == "structure_component_rescan":
            copied = copy.deepcopy(check)
            copied["check_id"] = f"minimized_{check['check_id']}"
            copied["check_type"] = "minimized_structure_component_rescan"
            copied["minimized_structure_manifest_path"] = "outputs.minimized_structure"
            copied["structure_path"] = "minimized_structure.pdb"
            out.append(copied)
        elif check.get("check_type") == "pdb_residue_state":
            copied = copy.deepcopy(check)
            copied["check_id"] = f"minimized_{check['check_id']}"
            copied["structure_manifest_path"] = "outputs.minimized_structure"
            copied["structure_path"] = "minimized_structure.pdb"
            out.append(copied)
        elif check.get("check_type") == "assembly_identity_check":
            copied = copy.deepcopy(check)
            copied["check_id"] = f"minimized_{check['check_id']}"
            copied["structure_manifest_path"] = "outputs.minimized_structure"
            out.append(copied)
    return out


def _task(
    task_id: str,
    title: str,
    pdb_source: str,
    task_text: str,
    checks: list[dict],
    *,
    priority: int,
    tags: list[str],
    references: list[dict],
    time_limit: int = 60,
    extra_outputs: list[str] | None = None,
    llm_rubrics: list[str] | None = None,
    prompt_extra: str = "",
) -> dict:
    required_outputs = list(COMMON_OUTPUTS)
    for rel in extra_outputs or []:
        if rel not in required_outputs:
            required_outputs.append(rel)
    deterministic_checks = [
        *checks,
        *_topology_minimization_checks(),
        *_minimized_persistence_checks(checks),
    ]
    return {
        "schema_version": "1.0",
        "task_id": task_id,
        "category": "system_preparation",
        "primary_score": "preparation",
        "secondary_scores": ["evidence_communication"],
        "execution_mode": "lite",
        "capability_tags": ["preparation_workflow", *tags],
        "environment_type": "artifact_only",
        "requires_tools": ["structure_preparation"],
        "evaluation_target": "agent_preparation_fidelity",
        "time_limit_minutes": time_limit,
        "failure_policy": {
            "blocked_by_missing_input_allowed": False,
            "insufficient_information_allowed": False,
        },
        "required_outputs": required_outputs,
        "scoring": {
            "deterministic_checks": deterministic_checks,
            "ground_truth_checks": [],
            "llm_judge_rubrics": llm_rubrics or ["provenance_traceability", "limitations"],
            "integrity_checks": [
                {
                    "check_id": "evidence_report_real_bytes",
                    "check_type": "artifact_min_bytes",
                    "path": "evidence_report.json",
                    "min_bytes": 250,
                },
                {
                    "check_id": "evidence_report_not_template",
                    "check_type": "template_markers",
                    "path": "evidence_report.json",
                    "forbid_markers": TEMPLATE_MARKERS,
                },
                {
                    "check_id": "prepared_structure_real_bytes",
                    "check_type": "status_artifact_floor",
                    "status_floor": {
                        "prepared_structure.pdb": 250,
                        "minimization_report.json": 100,
                    },
                },
            ],
            "integrity_policy": "warn",
        },
        "task_intent": f"{title}: {task_text}",
        "references": references,
        "prep_battery_priority": priority,
        "public_source": pdb_source,
        "public_prompt_extra": prompt_extra,
    }


